    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|unique:users',
            'email' => 'required|unique:users',
            'password' => 'required|min:6',
            'confirm' => 'required_with:password|same:password|min:6'
        ]);

        //No uso asignaciÃ³n masiva porque debo encriptar password
        //$user = User::create($request->all());

        $user = new User;
        $user->name = $request->get('name');
        $user->email = $request->get('email');
        $user->password = bcrypt($request->get('password'));
        //dd($user);
        $user->save();

        return redirect()->route('admin.users.index')->with('info','Usuario Creado. Para asignar rol debe editarlo');
    }

